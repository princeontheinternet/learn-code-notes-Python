# WAP to print "Hello World" in different cases.

# Program

print("HelloWorld in PascalCase")
print("helloWord in camelCase")
print("hello_world in snake_case")
print("hello-world in kebab-case")
print("helloworld in lowercase")
print("HELLOWORLD in UPPERCASE")

#   Explanation of the program:
#   In this program we are passing an argument of string literal 'Hello World' to print function.

#   Few Jargon:
#   1. literal: A value of some type.
#               Eg - Numeric Literal - 1, 42, 98.04, etc
#               Eg - String literal - 'Hello world', 'Python', 'abc', etc

#   2. Argument: A value passed to a function


#   OUTPUT:

# HelloWorld in PascalCase
# helloWord in camelCase
# hello_world in snake_case
# hello-world in kebab-case
# helloworld in lowercase
# HELLOWORLD in UPPERCASE
