#                   Sub Section 1: In Operator

# The following program demonstrate the use of in operator.

# "in" and "not in" are "Membership Operators"

fruit = "Watermelon"
letter = "w"

if letter in fruit.casefold():              # casefold method converts string into lower case
    print(letter in fruit.casefold())
else:
    print(letter in fruit.casefold())

#   Output:

# True
